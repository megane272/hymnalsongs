// Liste triée par titre pour la section alphabétique
List<Song> get songsAlphabetical {
  final list = List<Song>.from(songs);
  list.sort((a, b) => a.title.compareTo(b.title));
  return list;
}

// Liste triée par numéro pour la section numérique
List<Song> get songsNumerical {
  final list = songs.where((song) => song.number != null).toList();
  list.sort((a, b) => a.number!.compareTo(b.number!));
  return list;
}
